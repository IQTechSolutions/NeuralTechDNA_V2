namespace NeuralTech.Enums
{
    /// <summary>
    /// Represents titles that can be assigned to individuals.
    /// </summary>
    public enum Title
    {
        /// <summary>
        /// Title for a married woman.
        /// </summary>
        Mrs = 0,

        /// <summary>
        /// Title for a man.
        /// </summary>
        Mr = 1,

        /// <summary>
        /// Title for a woman, regardless of marital status.
        /// </summary>
        Ms = 2,

        /// <summary>
        /// Title for an unmarried woman.
        /// </summary>
        Miss = 3,

        /// <summary>
        /// Title for a professor.
        /// </summary>
        Prof = 4,

        /// <summary>
        /// Title for a doctor.
        /// </summary>
        Dr = 5,

        /// <summary>
        /// Title for a pastor.
        /// </summary>
        Pastor = 6,

        /// <summary>
        /// Title for a magistrate or judge.
        /// </summary>
        Me = 7
    }
}