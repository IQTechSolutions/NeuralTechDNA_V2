namespace NeuralTech.Enums
{
    /// <summary>
    /// Represents the marital status of an individual.
    /// </summary>
    public enum MaritalStatus
    {
        /// <summary>
        /// Marital status is unknown or not specified.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// The individual is single.
        /// </summary>
        Single = 1,

        /// <summary>
        /// The individual is married.
        /// </summary>
        Married = 2,

        /// <summary>
        /// The individual is engaged.
        /// </summary>
        Engaged = 3,

        /// <summary>
        /// The individual is divorced.
        /// </summary>
        Divorced = 4,

        /// <summary>
        /// The individual is widowed.
        /// </summary>
        Widowed = 5
    }
}